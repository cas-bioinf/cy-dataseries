package cz.cas.mbu.cytimeseries;

public class DataSeriesException extends RuntimeException {

	public DataSeriesException() {
		super();
	}

	public DataSeriesException(String message, Throwable cause) {
		super(message, cause);
	}

	public DataSeriesException(String message) {
		super(message);
	}

	public DataSeriesException(Throwable cause) {
		super(cause);
	}
 
}
