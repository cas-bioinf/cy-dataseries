package cz.cas.mbu.cytimeseries.dataimport;

import cz.cas.mbu.cytimeseries.DataSeriesException;

public class DataSeriesImportException extends DataSeriesException {

	public DataSeriesImportException() {
		super();
	}

	public DataSeriesImportException(String message, Throwable cause) {
		super(message, cause);
	}

	public DataSeriesImportException(String message) {
		super(message);
	}

	public DataSeriesImportException(Throwable cause) {
		super(cause);
	}
 
}
